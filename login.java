    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        UsuariosSQL modSql = new UsuariosSQL();
        usuarios mod = new usuarios();
        Date date = new Date();
        DateFormat fechaHora = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");

        String pass = new String(txtPassword.getPassword());

        if (!txtUsuario.getText().equals("") && !pass.equals("")) {
            String newPass = hash.sha1(pass);

            mod.setUsuario(txtUsuario.getText());
            mod.setPassword(newPass);
            mod.setLast_log(fechaHora.format(date));

            if (modSql.login(mod)) {
                frmLog = null; 
                this.dispose();
                
                if (frmGest == null) {
                    frmGest = new Gestor();
                    frmGest.setVisible(true);
                }
                else if (frmGest != null){
                    this.dispose();
                }

            } else {
                JOptionPane.showMessageDialog(null, "Los datos no son correctos");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Ingrese los datos");
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

