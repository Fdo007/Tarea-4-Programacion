        private void table_update()
        {
            int c;
             try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/loginuser", "root", "susano34");
            insert = con.prepareStatement("SELECT * FROM tipos_usuarios");
            
            ResultSet rs = insert.executeQuery();
            ResultSetMetaData rss = rs.getMetaData();
            c = rss.getColumnCount();
            
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            
           df.setRowCount(0);
           
           while(rs.next())
           {
               Vector v2 = new Vector();
               
               for(int a=1; a<=c; a++)
               {
                 v2.add(rs.getString("usuario"));
                 v2.add(rs.getString("password"));
                 v2.add(rs.getString("nombre"));
                 v2.add(rs.getString("apellido"));
                 v2.add(rs.getString("email"));
                 v2.add(rs.getInt("numero_telefono"));
                         
               }
               df.addRow(v2);
           }
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Gestor.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Gestor.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        }
